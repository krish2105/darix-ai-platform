export const dictionaries: Record<Locale, Dict> = { en, ar };

// Standalone lookup usable outside React (server-side PDF rendering,
// route handlers) as well as inside it — LanguageContext's `t()` is a thin
// wrapper around this bound to the current locale.
export const translate = (
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>
): string => {
  const dict = dictionaries[locale];
  let value = dict[key] ?? dictionaries.en[key] ?? key;
  if (vars) {
    for (const [varKey, varValue] of Object.entries(vars)) {
      value = value.replace(`{${varKey}}`, String(varValue));
    }
  }
  return value;
};
